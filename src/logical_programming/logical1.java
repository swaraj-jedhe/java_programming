package logical_programming;

public class logical1 {

	public static void main(String[] args) {
		
		int temp;
		int a =50;
		int b=60;
		System.out.println("before swapping");
		
		System.out.println("A "+a);
		System.out.println("B "+b);
		temp =a;// temp=50
		a=b;//a=60
		b=temp;//b=50
		System.out.println("after swapping");
		System.out.println("A "+a);
		System.out.println("B "+b);
		
		
	}
}
